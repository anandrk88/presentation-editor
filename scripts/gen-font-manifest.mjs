// Generate src/fonts/manifest.ts from public/fonts/fonts.css — the family/weight/
// italic → filename map the .pptx font-embedder needs at export time. Idempotent;
// re-run after changing the bundled fonts (install-fonts.mjs also emits this).
//
//   node scripts/gen-font-manifest.mjs

import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const css = readFileSync(resolve(root, "public", "fonts", "fonts.css"), "utf8");

const re = /font-family:"([^"]+)";font-style:(normal|italic);font-weight:(\d+);[^}]*?url\("([^"]+)"\)/g;
const faces = [];
let m;
while ((m = re.exec(css))) {
  faces.push({ family: m[1], weight: Number(m[3]), italic: m[2] === "italic", file: m[4] });
}
faces.sort((a, b) => a.family.localeCompare(b.family) || a.weight - b.weight || Number(a.italic) - Number(b.italic));

const tsDir = resolve(root, "src", "fonts");
mkdirSync(tsDir, { recursive: true });
writeFileSync(resolve(tsDir, "manifest.ts"),
  `// generated by scripts/gen-font-manifest.mjs — do not edit by hand\n` +
  `export interface FontFile { family: string; weight: number; italic: boolean; file: string }\n` +
  `export const FONT_FILES: FontFile[] = ${JSON.stringify(faces, null, 0)};\n`);

console.log(`Wrote src/fonts/manifest.ts — ${faces.length} faces across ${new Set(faces.map(f => f.family)).size} families`);
